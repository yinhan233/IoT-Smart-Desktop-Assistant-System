void OLED_Init(void) {
  HAL_Delay(100); // 等待屏幕上电稳定

  OLED_WriteCmd(0xAE); // 关闭显示
  OLED_WriteCmd(0xD5); // 设置时钟分频
  OLED_WriteCmd(0x80);
  OLED_WriteCmd(0xA8); // 设置多路复用率
  OLED_WriteCmd(0x3F);
  OLED_WriteCmd(0xD3); // 设置显示偏移
  OLED_WriteCmd(0x00);
  OLED_WriteCmd(0x40); // 设置起始行
  OLED_WriteCmd(0x8D); // 电荷泵设置
  OLED_WriteCmd(0x14); // 开启电荷泵
  OLED_WriteCmd(0x20); // 设置内存地址模式
  OLED_WriteCmd(0x02); // 页地址模式 (Page Addressing Mode)
  OLED_WriteCmd(0xA1); // 段重映射 (左右反转可改这里)
  OLED_WriteCmd(0xC8); // 扫描方向 (上下反转可改这里)
  OLED_WriteCmd(0xDA); // COM引脚配置
  OLED_WriteCmd(0x12);
  OLED_WriteCmd(0x81); // 对比度设置
  OLED_WriteCmd(0xCF);
  OLED_WriteCmd(0xD9); // 预充电周期
  OLED_WriteCmd(0xF1);
  OLED_WriteCmd(0xDB); // VCOMH电平
  OLED_WriteCmd(0x40);
  OLED_WriteCmd(0xA4); // 全屏显示开启 (A4正常, A5全亮)
  OLED_WriteCmd(0xA6); // 正常显示 (A7反色)
  OLED_WriteCmd(0xAF); // 开启显示

  // 清屏代码（可选，防止上电花屏）
  for(int i=0; i<8; i++) {
    OLED_WriteCmd(0xB0 + i); // 设置页地址 (0-7)
    OLED_WriteCmd(0x00);      // 设置列低地址
    OLED_WriteCmd(0x10);      // 设置列高地址
    for(int j=0; j<128; j++) OLED_WriteData(0x00);
  }
}
